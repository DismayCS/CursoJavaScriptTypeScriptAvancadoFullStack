//objeto MATH

let num1 = 9.5;

//arredondando numeros para baixo
console.log(Math.floor(num1));
//arredondando numeros para cima
console.log(Math.ceil(num1));
//arredondando numeros para o mais proximo
console.log(Math.round(num1));
