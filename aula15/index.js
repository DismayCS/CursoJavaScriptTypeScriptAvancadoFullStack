console.log(Math.max(1,2,3,4,5,6,-1,-2,-4,-8,10));
//retorna o menor numero de uma sequencia
console.log(Math.min(1,2,3,4,5,6,-1,-2,-4,-8,10));
//numero aleatorio
console.log(Math.random());